#include "ft_printf.h"

int	ft_putstr(char *s)
{
	int		i;
	char	*a;

	a = "(null)";
	i = -1;
	if (!s)
	{
		while (a[++i])
			write(1, &a[i], 1);
		return (i);
	}
	while (s[++i])
		write(1, &s[i], 1);
	return (i);
}
